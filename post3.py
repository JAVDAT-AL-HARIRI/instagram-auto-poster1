def build_hashtags(rng, cfg):
    fixed = cfg.get("fixed_hashtag", "")
    priority_pool = [x for x in cfg.get("priority_hashtags", []) if x != fixed]
    local_pool = [x for x in LOCAL_HASHTAGS if x != fixed]
    normal_pool = [
        x for x in cfg.get("hashtags", [])
        if x != fixed and x not in priority_pool and x not in local_pool
    ]

    tags = []
    if fixed:
        tags.append(fixed)

    tags += safe_sample(rng, priority_pool, SETTINGS["PRIORITY_HASHTAGS_PER_POST"])
    tags += safe_sample(rng, local_pool, SETTINGS["LOCAL_HASHTAGS_PER_POST"])

    remaining = SETTINGS["HASHTAGS_PER_POST"] - len(unique_list(tags))
    if remaining > 0:
        tags += safe_sample(rng, normal_pool, remaining)

    tags = unique_list(tags)
    if SETTINGS["SHUFFLE_HASHTAGS"]:
        fixed_tag = fixed if fixed in tags else None
        rest = [x for x in tags if x != fixed_tag]
        rng.shuffle(rest)
        tags = ([fixed_tag] if fixed_tag else []) + rest

    return tags[:SETTINGS["HASHTAGS_PER_POST"]]


