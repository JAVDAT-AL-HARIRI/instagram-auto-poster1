def build_caption(rng, brand_key, post_index, scheduled_time, image_name, used_hashes, recent_captions):
    attempts = int(SETTINGS["MAX_CAPTION_ATTEMPTS"])
    for attempt in range(attempts):
        # rng فرعي لكل محاولة حتى لا تتشابه النتائج بطريقة غريبة
        seed = short_hash(SETTINGS["RUN_SEED"], brand_key, post_index, scheduled_time, image_name, attempt, length=16)
        local_rng = random.Random(seed)

        caption, metadata = build_caption_once(local_rng, brand_key, post_index, scheduled_time, image_name)
        cap_hash = fingerprint(caption)

        if SETTINGS["STRICT_EXACT_UNIQUE"] and cap_hash in used_hashes:
            continue

        if too_similar(caption, recent_captions):
            continue

        return caption, cap_hash, metadata, attempt

    raise ValueError(
        "لم أستطع توليد وصف فريد كفاية. الحل: زِد الكلمات والقوالب أو عطّل SIMILARITY_CHECK_ENABLED مؤقتاً."
    )


