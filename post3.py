def parse_time_slot(date_obj, slot):
    hour, minute = map(int, slot.split(":"))
    return date_obj.replace(hour=hour, minute=minute, second=0, microsecond=0)


def schedule_for_index(i, rng):
    start = datetime.strptime(SETTINGS["START_DATE"], "%Y-%m-%d %H:%M")

    if SETTINGS["SCHEDULE_MODE"] == "interval":
        dt = start + timedelta(hours=i * float(SETTINGS["POST_INTERVAL_HOURS"]))
    else:
        slots = SETTINGS["DAILY_TIME_SLOTS"]
        if not slots:
            raise ValueError("DAILY_TIME_SLOTS فارغة.")
        day_offset = i // len(slots)
        slot_index = i % len(slots)
        base_day = (start + timedelta(days=day_offset)).replace(hour=0, minute=0, second=0, microsecond=0)
        dt = parse_time_slot(base_day, slots[slot_index])
        if dt < start:
            dt += timedelta(days=1)

    jitter = int(SETTINGS["TIME_JITTER_MINUTES"])
    if jitter > 0:
        dt += timedelta(minutes=rng.randint(-jitter, jitter))
    return dt


