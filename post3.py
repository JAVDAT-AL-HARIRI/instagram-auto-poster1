class ImageManager:
    def __init__(self, rng):
        self.rng = rng
        self.last_images = defaultdict(lambda: deque(maxlen=int(SETTINGS["MIN_GAP_SAME_IMAGE_PER_BRAND"])))
        self.day_images = defaultdict(set)
        self.cycles = {}
        self.sequential_index = defaultdict(int)

    def _cycle_key(self, brand_key):
        return f"{brand_key}::cycle"

    def pick(self, brand_key, cfg, scheduled_time):
        images = get_allowed_images(brand_key, cfg)
        strategy = SETTINGS["IMAGE_STRATEGY"]
        day_key = scheduled_time.strftime("%Y-%m-%d")
        blocked_recent = set(self.last_images[brand_key])
        blocked_day = self.day_images[(brand_key, day_key)] if SETTINGS["AVOID_SAME_IMAGE_SAME_DAY"] else set()

        if strategy == "sequential":
            img = images[self.sequential_index[brand_key] % len(images)]
            self.sequential_index[brand_key] += 1
            return self._remember(brand_key, day_key, img)

        if strategy == "random_gap":
            choices = [x for x in images if x not in blocked_recent and x not in blocked_day]
            if not choices:
                choices = [x for x in images if x not in blocked_recent]
            if not choices:
                choices = images
            return self._remember(brand_key, day_key, self.rng.choice(choices))

        # default: shuffle_cycle
        key = self._cycle_key(brand_key)
        if key not in self.cycles or not self.cycles[key]:
            cycle = images[:]
            self.rng.shuffle(cycle)
            self.cycles[key] = deque(cycle)

        for _ in range(len(images) + 5):
            img = self.cycles[key].popleft()
            self.cycles[key].append(img)
            if img not in blocked_recent and img not in blocked_day:
                return self._remember(brand_key, day_key, img)

        for _ in range(len(images) + 5):
            img = self.cycles[key].popleft()
            self.cycles[key].append(img)
            if img not in blocked_recent:
                return self._remember(brand_key, day_key, img)

        img = self.cycles[key].popleft()
        self.cycles[key].append(img)
        return self._remember(brand_key, day_key, img)

    def _remember(self, brand_key, day_key, image_name):
        self.last_images[brand_key].append(image_name)
        self.day_images[(brand_key, day_key)].add(image_name)
        return image_name


