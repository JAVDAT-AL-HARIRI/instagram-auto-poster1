def get_allowed_images(brand_key, cfg):
    images = list(cfg.get("images", []))
    only = SETTINGS.get("ONLY_IMAGES", {}).get(brand_key, [])
    excluded = set(SETTINGS.get("EXCLUDED_IMAGES", {}).get(brand_key, []))
    if only:
        images = [img for img in images if img in set(only)]
    images = [img for img in images if img not in excluded]
    return unique_list(images)


