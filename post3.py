ARABIC_DIACRITICS = re.compile(r"[\u0617-\u061A\u064B-\u0652]")


def clean_text(text):
    text = text.replace("\r\n", "\n").replace("\r", "\n")
    text = re.sub(r"[ \t]+", " ", text)
    text = re.sub(r" *\n *", "\n", text)
    text = re.sub(r"\n{3,}", "\n\n", text)
    return text.strip()


def normalize_arabic(text):
    text = ARABIC_DIACRITICS.sub("", text)
    text = text.replace("أ", "ا").replace("إ", "ا").replace("آ", "ا")
    text = text.replace("ى", "ي").replace("ة", "ه")
    text = text.replace("ؤ", "و").replace("ئ", "ي")
    text = re.sub(r"[^\w\s#]+", " ", text, flags=re.UNICODE)
    text = re.sub(r"\s+", " ", text)
    return text.strip().lower()


def fingerprint(text):
    normalized = normalize_arabic(text)
    return hashlib.sha256(normalized.encode("utf-8")).hexdigest()


def short_hash(*parts, length=8):
    raw = "||".join(map(str, parts))
    return hashlib.sha256(raw.encode("utf-8")).hexdigest()[:length]


def base36(num):
    alphabet = "0123456789abcdefghijklmnopqrstuvwxyz"
    num = abs(int(num))
    if num == 0:
        return "0"
    out = []
    while num:
        num, rem = divmod(num, 36)
        out.append(alphabet[rem])
    return "".join(reversed(out))


def unique_list(items):
    seen = set()
    result = []
    for item in items:
        item = clean_text(str(item))
        if item and item not in seen:
            seen.add(item)
            result.append(item)
    return result


def safe_sample(rng, items, count):
    items = unique_list(items)
    count = min(count, len(items))
    if count <= 0:
        return []
    return rng.sample(items, count)


def safe_choice(rng, items, exclude=None):
    exclude = set(exclude or [])
    choices = [x for x in unique_list(items) if x not in exclude]
    if not choices:
        choices = unique_list(items)
    if not choices:
        return ""
    return rng.choice(choices)


def weighted_choice(rng, weights):
    total = sum(max(0.0, float(v)) for v in weights.values())
    if total <= 0:
        return rng.choice(list(weights.keys()))
    point = rng.uniform(0, total)
    current = 0.0
    for key, weight in weights.items():
        current += max(0.0, float(weight))
        if current >= point:
            return key
    return list(weights.keys())[-1]


def count_word(text, phrase):
    if not phrase:
        return 0
    return normalize_arabic(text).count(normalize_arabic(phrase))


def apply_safe_claim_rules(text):
    if not SETTINGS["BLOCK_UNSAFE_CLAIMS"]:
        return text
    for bad, good in UNSAFE_CLAIMS_REPLACEMENTS.items():
        text = text.replace(bad, good)
    return text


def trim_to_max(text, max_chars):
    """
    تقليل الطول بدون قص الهاشتاق من المنتصف.
    يحذف الأسطر الأقل أهمية أولاً، وإذا اضطر يقص من جسم الوصف فقط ويبقي الهاشتاقات كاملة.
    """
    text = clean_text(text)
    if len(text) <= max_chars:
        return text

    hashtag_lines = []
    body_lines = []
    for line in text.split("\n"):
        if line.strip().startswith("#"):
            hashtag_lines.append(line.strip())
        else:
            body_lines.append(line)

    body = clean_text("\n".join(body_lines))
    hashtags = "\n".join(unique_list(hashtag_lines))
    hashtag_block = ("\n\n" + hashtags) if hashtags else ""

    removable_starts = (
        "كلمات بحث مساعدة:",
        "عبارات مرتبطة:",
        "تفصيلة هذا المنشور:",
        "ملاحظة تسوق سريعة:",
        "نقطة توفير مهمة:",
        "تذكير صغير:",
    )

    body_parts = body.split("\n")
    filtered = []
    for line in body_parts:
        if line.strip().startswith(removable_starts):
            candidate_body = clean_text("\n".join(filtered + body_parts[body_parts.index(line) + 1:]))
            if len(candidate_body + hashtag_block) <= max_chars:
                continue
        filtered.append(line)

    body = clean_text("\n".join(filtered))
    if len(body + hashtag_block) <= max_chars:
        return clean_text(body + hashtag_block)

    # احذف آخر فقرات اختيارية من الجسم فقط
    paragraphs = body.split("\n\n")
    while len(paragraphs) > 2 and len(clean_text("\n\n".join(paragraphs)) + hashtag_block) > max_chars:
        paragraphs.pop(-1)

    body = clean_text("\n\n".join(paragraphs))
    if len(body + hashtag_block) <= max_chars:
        return clean_text(body + hashtag_block)

    # آخر حل: قص جسم النص فقط مع ترك الهاشتاقات سليمة
    allowed_body_len = max(200, max_chars - len(hashtag_block) - 4)
    body = body[:allowed_body_len].rstrip() + "..."
    return clean_text(body + hashtag_block)


def similarity(a, b):
    return SequenceMatcher(None, normalize_arabic(a), normalize_arabic(b)).ratio()


def too_similar(candidate, recent_captions):
    if not SETTINGS["SIMILARITY_CHECK_ENABLED"]:
        return False
    threshold = float(SETTINGS["SIMILARITY_THRESHOLD"])
    for old in recent_captions:
        if similarity(candidate, old) >= threshold:
            return True
    return False


