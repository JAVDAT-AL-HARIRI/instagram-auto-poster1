def brand_for_index(i, rng, recent_brands):
    mode = SETTINGS["BRAND_ORDER_MODE"]
    if mode == "alternating":
        seq = SETTINGS["BRAND_SEQUENCE"]
        return seq[i % len(seq)]

    if mode == "weighted_random":
        for _ in range(20):
            brand = weighted_choice(rng, SETTINGS["BRAND_WEIGHTS"])
            if len(recent_brands) < SETTINGS["MAX_SAME_BRAND_IN_ROW"]:
                return brand
            if not all(x == brand for x in list(recent_brands)[-SETTINGS["MAX_SAME_BRAND_IN_ROW"]:]):
                return brand
        seq = SETTINGS["BRAND_SEQUENCE"]
        return seq[i % len(seq)]

    raise ValueError("BRAND_ORDER_MODE غير معروف.")


