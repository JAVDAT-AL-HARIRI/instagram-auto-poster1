def generate_posts():
    extend_brand_data()
    validate_settings()

    rng = random.Random(SETTINGS["RUN_SEED"])
    image_manager = ImageManager(rng)

    used_hashes_file = SETTINGS["USED_HASHES_FILE"]
    used_hashes = load_used_hashes(used_hashes_file)
    before_hash_count = len(used_hashes)

    recent_captions = deque(maxlen=int(SETTINGS["RECENT_CAPTIONS_TO_COMPARE"]))
    recent_brands = deque(maxlen=int(SETTINGS["MAX_SAME_BRAND_IN_ROW"]) + 1)

    rows = []
    stats = {
        "brand_counts": defaultdict(int),
        "image_counts": defaultdict(int),
        "attempts_total": 0,
        "new_hashes": 0,
    }

    for i in range(int(SETTINGS["TOTAL_POSTS"])):
        scheduled_time = schedule_for_index(i, rng)
        brand_key = brand_for_index(i, rng, recent_brands)
        cfg = BRANDS[brand_key]

        image_name = image_manager.pick(brand_key, cfg, scheduled_time)
        image_url = f"{SETTINGS['GITHUB_BASE_URL'].rstrip('/')}/{cfg['folder']}/{image_name}"

        caption, cap_hash, metadata, attempts_used = build_caption(
            rng,
            brand_key,
            i,
            scheduled_time,
            image_name,
            used_hashes,
            recent_captions,
        )

        used_hashes.add(cap_hash)
        recent_captions.append(caption)
        recent_brands.append(brand_key)

        stats["brand_counts"][brand_key] += 1
        stats["image_counts"][f"{brand_key}/{image_name}"] += 1
        stats["attempts_total"] += attempts_used + 1

        rows.append({
            "brand": brand_key,
            "brand_ar": cfg["brand_ar"],
            "code": cfg["code"],
            "image_name": image_name,
            "image_url": image_url,
            "caption": caption,
            "scheduled_time": scheduled_time.strftime("%Y-%m-%d %H:%M"),
            "posted": "no",
            "caption_hash": cap_hash,
            "title": metadata["title"],
            "main_keywords": " | ".join(metadata["main_keywords"]),
            "secondary_keywords": " | ".join(metadata["secondary_keywords"]),
            "long_tail_keywords": " | ".join(metadata["long_tail_keywords"]),
            "hashtags": " ".join(metadata["hashtags"]),
        })

    output_file = SETTINGS["OUTPUT_FILE"]
    fieldnames = [
        "brand",
        "brand_ar",
        "code",
        "image_name",
        "image_url",
        "caption",
        "scheduled_time",
        "posted",
        "caption_hash",
        "title",
        "main_keywords",
        "secondary_keywords",
        "long_tail_keywords",
        "hashtags",
    ]

    with open(output_file, "w", newline="", encoding="utf-8-sig") as file:
        writer = csv.DictWriter(file, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)

    save_used_hashes(used_hashes_file, used_hashes)

    stats["new_hashes"] = len(used_hashes) - before_hash_count
    report = {
        "output_file": output_file,
        "total_posts": len(rows),
        "unique_new_captions": stats["new_hashes"],
        "used_hashes_file": used_hashes_file,
        "total_hashes_saved": len(used_hashes),
        "schedule_mode": SETTINGS["SCHEDULE_MODE"],
        "start_date": SETTINGS["START_DATE"],
        "daily_time_slots": SETTINGS["DAILY_TIME_SLOTS"],
        "post_interval_hours": SETTINGS["POST_INTERVAL_HOURS"],
        "hashtags_per_post": SETTINGS["HASHTAGS_PER_POST"],
        "saudi_style_level": SETTINGS["SAUDI_STYLE_LEVEL"],
        "image_strategy": SETTINGS["IMAGE_STRATEGY"],
        "brand_counts": dict(stats["brand_counts"]),
        "average_caption_attempts": round(stats["attempts_total"] / max(1, len(rows)), 3),
        "settings_summary": {
            "max_caption_chars": SETTINGS["MAX_CAPTION_CHARS"],
            "similarity_check_enabled": SETTINGS["SIMILARITY_CHECK_ENABLED"],
            "similarity_threshold": SETTINGS["SIMILARITY_THRESHOLD"],
            "add_unique_reference_line": SETTINGS["ADD_UNIQUE_REFERENCE_LINE"],
        }
    }

    with open(SETTINGS["REPORT_FILE"], "w", encoding="utf-8") as f:
        json.dump(report, f, ensure_ascii=False, indent=2)

    print(f"Done: {output_file} created successfully")
    print(f"Total posts: {len(rows)}")
    print(f"New unique captions: {stats['new_hashes']}")
    print(f"Saved hashes: {len(used_hashes)}")
    print(f"Report: {SETTINGS['REPORT_FILE']}")
    print(f"Brand counts: {dict(stats['brand_counts'])}")
    return rows, report


